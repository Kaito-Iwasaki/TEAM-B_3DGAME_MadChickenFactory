//=====================================================================
// 初期化処理
//=====================================================================
void InitGame(void)
{
	// ライトをオフにする
	//GetDevice()->SetRenderState(D3DRS_LIGHTING, FALSE);

	// 各オブジェクトの初期化処理
	InitCamera();		// カメラ
	InitPlayer();		// プレイヤー
	InitField();		// フィールド
	InitLight();		//ライト
}

//=====================================================================
// 終了処理
//=====================================================================
void UninitGame(void)
{
	// 各オブジェクトの終了処理
	UninitCamera();		// カメラ
	UninitPlayer();		// プレイヤー
	UninitField();		// フィールド
	UninitLight();		//ライト
}

//=====================================================================
// 更新処理
//=====================================================================
void UpdateGame(void)
{
	// デバッグ表示
	PrintDebugProc("ゲーム画面\n");

	// 各オブジェクトの更新処理
	UpdateCamera();		// カメラ
	UpdatePlayer();		// プレイヤー
	UpdateField();		// フィールド
	UpdateLight();		//ライト
}

//=====================================================================
// 描画処理
//=====================================================================
void DrawGame(void)
{
	// ゲームカメラの設定
	SetCamera(CAMERATYPE_GAME);

	// 各オブジェクトの描画処理
	DrawPlayer();		// プレイヤー
	DrawField();		// フィールド
}